package com.example.actividad1_ev2;

public class Capitales {

    String Continentes;
    String paises;
    String capitalP;

    public Capitales(String Continentes, String paises, String capitalP){
        this.Continentes=Continentes;
        this.capitalP=Continentes;
        this.capitalP=capitalP;



    }

    public String getContinentes() {
        return Continentes;
    }

    public void setContinentes(String continentes) {
        Continentes = continentes;
    }

    public String getPaises() {
        return paises;
    }

    public void setPaises(String paises) {
        this.paises = paises;
    }

    public String getCapitalP() {
        return capitalP;
    }

    public void setCapitalP(String capitalP) {
        this.capitalP = capitalP;
    }
}
